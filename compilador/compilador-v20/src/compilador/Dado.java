package compilador;

public class Dado {
	private int indiceDado;
	private String dado;
	private int tamDado;
	private String tipo;

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public int getIndiceDado() {
		return indiceDado;
	}

	public void setIndiceDado(int indiceDado) {
		this.indiceDado = indiceDado;
	}

	public String getDado() {
		return dado;
	}

	public void setDado(String dado) {
		this.dado = dado;
	}

	public int getTamDado() {
		return tamDado;
	}

	public void setTamDado(int tamDado) {
		this.tamDado = tamDado;
	}

}
